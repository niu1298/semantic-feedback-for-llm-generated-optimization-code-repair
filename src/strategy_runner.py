    def _effective_advisory_mode(self) -> str:
        if self.config.advisory_mode is not None:
            mode = str(self.config.advisory_mode)
        elif self.config.strategy == "semantic_execution":
            mode = "hard_gate"
        elif self.config.strategy == "semantic_advisory_execution":
            mode = "diagnosis_only"
        else:
            mode = "none"
        if mode not in {"none", "diagnosis_only", "retrospective_gate", "hard_gate"}:
            raise ValueError(f"Unsupported advisory_mode: {mode}")
        return mode

    def _include_static_checks_in_repair_prompt(self) -> bool:
        if self.config.include_static_checks_in_repair_prompt is not None:
            return self.config.include_static_checks_in_repair_prompt
        return bool(self.config.static_checks_enabled)

    def _include_semantic_diagnosis_in_repair_prompt(self) -> bool:
        if self.config.include_semantic_diagnosis_in_repair_prompt is not None:
            return self.config.include_semantic_diagnosis_in_repair_prompt
        return self._effective_advisory_mode() != "none"

    def _load_selected_problems(
        self,
        adapter: ORThoughtAdapter,
        run_dir: Path,
    ) -> list[dict[str, Any]]:
        if self.config.problem_ids is not None:
            all_problems = adapter.load_problems()
            problems = _select_problem_ids(all_problems, self.config.problem_ids)
            selection_method = "problem_ids"
        elif self.config.max_problems_per_band is not None:
            all_problems = adapter.load_problems()
            problems = _select_stratified_problems(
                all_problems,
                max_problems_per_band=self.config.max_problems_per_band,
                random_seed=self.config.random_seed,
            )
            selection_method = "max_problems_per_band"
        else:
            problems = adapter.load_problems(
                limit=self.config.problem_limit,
                offset=self.config.problem_offset,
            )
            selection_method = "limit_offset"
        _write_selected_problems_manifest(
            run_dir,
            config=self.config,
            selection_method=selection_method,
            problems=problems,
        )
        return problems

    def _semantic_gate_decision(
        self,
        problem: dict[str, object],
        extracted_code: str,
        round_index: int,
        semantic_prompt_path: Path | None = None,
        static_checks: dict[str, Any] | list[dict[str, Any]] | None = None,
    ) -> dict[str, Any]:
        rule_result = semantic_rule_precheck(extracted_code)
        if not rule_result.passed:
            _write_semantic_prompt_artifact(
                semantic_prompt_path,
                problem=problem,
                extracted_code=extracted_code,
                static_checks=static_checks,
                prompt_style=self.config.semantic_prompt_style,
                sent=False,
                note="Semantic LLM prompt was not sent because the local rule precheck rejected first.",
            )
            semantic_payload = {
                "passed": False,
                "skipped": True,
                "status": "rule_semantic_reject",
                "score": 0.0,
                "should_execute": False,
                "missing_constraints": [],
                "wrong_objective": False,
                "variable_issues": [],
                "gurobi_api_issues": list(rule_result.issues),
                "output_issues": [],
                "feedback": rule_result.feedback,
                "raw_response": "",
                "rule_precheck": rule_result.to_dict(),
                "semantic_gate_stage": "rule",
            }
            return _gate_decision_payload(
                semantic_payload,
                should_execute=False,
                semantic_calls=0,
                rule_semantic_reject=True,
                rule_semantic_issues=rule_result.issues,
                semantic_gate_stage="rule",
            )

        if not self.config.semantic_cascade_enabled:
            result = self._call_semantic_checker(
                problem=problem,
                extracted_code=extracted_code,
                provider=self.config.semantic_provider or self.config.llm_provider,
                model=self.config.semantic_model or self.config.llm_model,
                threshold=float(self.config.semantic_threshold or 0.6),
                round_index=round_index,
                stage="semantic_check",
                semantic_prompt_path=semantic_prompt_path,
                static_checks=static_checks,
            )
            payload = _semantic_result_to_dict(result)
            payload["semantic_gate_stage"] = "single"
            gate_should_execute = (
                bool(payload.get("should_execute"))
                if self._effective_advisory_mode() == "hard_gate"
                else bool(payload.get("passed"))
            )
            return _gate_decision_payload(
                payload,
                should_execute=gate_should_execute,
                semantic_calls=1,
                semantic_gate_stage="single",
            )

        fast_result = self._call_semantic_checker(
            problem=problem,
            extracted_code=extracted_code,
            provider=(
                self.config.semantic_fast_provider
                or self.config.semantic_provider
                or self.config.llm_provider
            ),
            model=(
                self.config.semantic_fast_model
                or self.config.semantic_model
                or self.config.llm_model
            ),
            threshold=self.config.semantic_high_threshold,
            round_index=round_index,
            stage="semantic_fast_check",
            semantic_prompt_path=_semantic_stage_prompt_path(semantic_prompt_path, "semantic_fast_check"),
            static_checks=static_checks,
        )
        fast_payload = _semantic_result_to_dict(fast_result)
        fast_score = _optional_float(fast_payload.get("score")) or 0.0
        fast_should_execute = bool(fast_payload.get("should_execute"))
        if not fast_should_execute or fast_score < self.config.semantic_low_threshold:
            fast_payload["semantic_gate_stage"] = "fast_reject"
            return _gate_decision_payload(
                fast_payload,
                should_execute=False,
                semantic_calls=1,
                fast_semantic_calls=1,
                fast_semantic_score=fast_score,
                cascade_fast_reject=True,
                semantic_gate_stage="fast_reject",
            )
        if fast_score >= self.config.semantic_high_threshold:
            fast_payload["semantic_gate_stage"] = "fast_accept"
            return _gate_decision_payload(
                fast_payload,
                should_execute=True,
                semantic_calls=1,
                fast_semantic_calls=1,
                fast_semantic_score=fast_score,
                cascade_fast_accept=True,
                semantic_gate_stage="fast_accept",
            )

        strong_result = self._call_semantic_checker(
            problem=problem,
            extracted_code=extracted_code,
            provider=(
                self.config.semantic_strong_provider
                or self.config.semantic_provider
                or self.config.llm_provider
            ),
            model=(
                self.config.semantic_strong_model
                or self.config.semantic_model
                or self.config.llm_model
            ),
            threshold=float(self.config.semantic_threshold or 0.6),
            round_index=round_index,
            stage="semantic_strong_check",
            semantic_prompt_path=_semantic_stage_prompt_path(semantic_prompt_path, "semantic_strong_check"),
            static_checks=static_checks,
        )
        strong_payload = _semantic_result_to_dict(strong_result)
        strong_score = _optional_float(strong_payload.get("score"))
        strong_payload["semantic_gate_stage"] = "strong"
        strong_payload["fast_semantic_result"] = fast_payload
        return _gate_decision_payload(
            strong_payload,
            should_execute=bool(strong_payload.get("passed")),
            semantic_calls=2,
            fast_semantic_calls=1,
            strong_semantic_calls=1,
            fast_semantic_score=fast_score,
            strong_semantic_score=strong_score,
            cascade_escalated=True,
            semantic_gate_stage="strong",
        )

    def _call_semantic_checker(
        self,
        problem: dict[str, object],
        extracted_code: str,
        provider: str,
        model: str,
        threshold: float,
        round_index: int | None = None,
        stage: str = "semantic_check",
        semantic_prompt_path: Path | None = None,
        static_checks: dict[str, Any] | list[dict[str, Any]] | None = None,
        prompt_style: str | None = None,
        max_tokens: int | None = None,
    ) -> Any:
        try:
            effective_prompt_style = prompt_style or self.config.semantic_prompt_style
            effective_max_tokens = max_tokens or self.config.semantic_max_tokens or self.config.max_tokens
            _write_semantic_prompt_artifact(
                semantic_prompt_path,
                problem=problem,
                extracted_code=extracted_code,
                static_checks=static_checks if self.config.include_static_checks_in_advisor_prompt else None,
                prompt_style=effective_prompt_style,
                sent=True,
            )
            with cost_logging_context(
                strategy=self.config.strategy,
                stage=stage,
                problem_id=str(problem["problem_id"]),
                round_index=round_index,
            ):
                return self.semantic_checker_fn(
                    problem_text=str(problem["problem_text"]),
                    generated_code=extracted_code,
                    provider=provider,
                    model=model,
                    threshold=threshold,
                    request_timeout_seconds=self.config.request_timeout_seconds,
                    max_retries=self.config.max_retries,
                    temperature=self.config.temperature,
                    max_tokens=effective_max_tokens,
                    thinking=self.config.thinking,
                    reasoning_effort=self.config.reasoning_effort,
                    llm_client=self.semantic_client,
                    static_checks=static_checks if self.config.include_static_checks_in_advisor_prompt else None,
                    prompt_style=effective_prompt_style,
                    round_index=round_index or 0,
                    advisor_name=f"{provider}:{model}",
                )
        except Exception as exc:
            return _semantic_error_dict(exc)

    def _maybe_generate_intended_spec(
        self,
        *,
        problem: dict[str, object],
        artifact_dir: Path,
        round_index: int,
    ) -> FormulationSpecResult | None:
        if not self.config.use_formulation_spec:
            return None
        path = artifact_dir / "intended_formulation_spec.json"
        try:
            client = self._make_aux_client(
                provider=self.config.spec_provider,
                model=self.config.spec_model,
                max_tokens=self.config.spec_max_tokens,
            )
            with cost_logging_context(
                strategy=self.config.strategy,
                stage="intended_spec_generation",
                problem_id=str(problem["problem_id"]),
                round_index=round_index,
            ):
                result = generate_intended_formulation_spec(
                    str(problem["problem_text"]),
                    str(problem["problem_id"]),
                    self.config,
                    client,
                )
        except Exception as exc:
            result = parse_formulation_spec_response(
                "",
                problem_id=str(problem["problem_id"]),
                source="intended",
                parse_debug={"provider_error": f"{exc.__class__.__name__}: {exc}"},
            )
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(json.dumps(result.to_dict(), indent=2, sort_keys=True), encoding="utf-8")
        return result

