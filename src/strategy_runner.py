def _should_execute(execute_policy: str, static_result: Any) -> bool:
    if execute_policy == "never":
        return False
    if execute_policy == "static_pass":
        return bool(static_result.passed)
    if execute_policy == "compile_ok":
        return bool(static_result.signals.get("python_compile_ok"))
    if execute_policy == "always":
        return True
    if execute_policy == "semantic_gate":
        return False
    if execute_policy == "semantic_advisory":
        return False
    if execute_policy == "semantic_retrospective_gate":
        return False
    raise ValueError(f"Unknown execute_policy: {execute_policy}")


def _write_semantic_prompt_artifact(
    path: Path | None,
    *,
    problem: dict[str, object],
    extracted_code: str,
    static_checks: dict[str, Any] | list[dict[str, Any]] | None = None,
    prompt_style: str = "default",
    sent: bool,
    note: str | None = None,
) -> None:
    if path is None:
        return
    messages = build_semantic_judge_prompt(
        problem_text=str(problem["problem_text"]),
        generated_code=extracted_code,
        static_checks=static_checks,
        prompt_style=prompt_style,
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    rendered = render_messages(messages)
    if sent:
        path.write_text(rendered, encoding="utf-8")
        return
    prefix = note or "Semantic LLM prompt was not sent."
    path.write_text(f"{prefix}\n\nWould-be semantic judge messages:\n{rendered}", encoding="utf-8")


def _semantic_stage_prompt_path(path: Path | None, stage: str) -> Path | None:
    if path is None:
        return None
    return path.with_name(f"{stage}_prompt.txt")


