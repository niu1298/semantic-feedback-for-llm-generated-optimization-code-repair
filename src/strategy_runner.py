def _select_problem_ids(
    problems: list[dict[str, Any]],
    requested_ids: list[str],
) -> list[dict[str, Any]]:
    by_id = {str(problem["problem_id"]): problem for problem in problems}
    missing = [problem_id for problem_id in requested_ids if problem_id not in by_id]
    if missing:
        raise ValueError(f"Configured problem_ids not found: {', '.join(missing)}")
    return [by_id[problem_id] for problem_id in requested_ids]


def _select_stratified_problems(
    problems: list[dict[str, Any]],
    *,
    max_problems_per_band: int,
    random_seed: int | None,
) -> list[dict[str, Any]]:
    if max_problems_per_band < 1:
        raise ValueError("max_problems_per_band must be >= 1.")
    rng = random.Random(random_seed)
    by_band: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for problem in problems:
        by_band[_problem_band(problem)].append(problem)

    selected: list[dict[str, Any]] = []
    for band in ("band1", "band2", "band3", "band4", "unknown"):
        band_problems = by_band.get(band, [])
        if not band_problems:
            continue
        count = min(max_problems_per_band, len(band_problems))
        selected.extend(rng.sample(band_problems, count))
    return sorted(selected, key=_problem_difficulty_rank)


def _write_selected_problems_manifest(
    run_dir: Path,
    *,
    config: ExperimentConfig,
    selection_method: str,
    problems: list[dict[str, Any]],
) -> None:
    band_counts = Counter(_problem_band(problem) for problem in problems)
    payload = {
        "selection_method": selection_method,
        "dataset_name": config.dataset_name,
        "config_name": _config_name(config),
        "strategy_name": config.strategy,
        "advisory_mode": config.advisory_mode,
        "problem_limit": config.problem_limit,
        "problem_offset": config.problem_offset,
        "problem_ids_configured": list(config.problem_ids) if config.problem_ids is not None else None,
        "max_problems_per_band": config.max_problems_per_band,
        "random_seed": config.random_seed,
        "problem_count": len(problems),
        "problem_ids": [str(problem["problem_id"]) for problem in problems],
        "band_counts": dict(sorted(band_counts.items())),
    }
    path = run_dir / "selected_problems.json"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")


def _problem_band(problem: dict[str, Any]) -> str:
    metadata = problem.get("metadata")
    rank = metadata.get("difficulty_rank") if isinstance(metadata, dict) else None
    return _band_for_rank(rank) or "unknown"


def _problem_difficulty_rank(problem: dict[str, Any]) -> tuple[int, str]:
    metadata = problem.get("metadata")
    rank = metadata.get("difficulty_rank") if isinstance(metadata, dict) else None
    try:
        numeric_rank = int(rank)
    except (TypeError, ValueError):
        numeric_rank = 10**9
    return numeric_rank, str(problem.get("problem_id", ""))


def _config_name(config: ExperimentConfig) -> str:
    if config.experiment_name:
        return str(config.experiment_name)
    raw_name = config.raw.get("config_name")
    if raw_name:
        return str(raw_name)
    raw_stem = config.raw.get("_config_stem")
    if raw_stem:
        return str(raw_stem)
    return config.strategy


def _first_valid_round(rounds: list[RoundResult]) -> int | None:
    for round_result in rounds:
        if round_result.valid:
            return round_result.round_index
    return None


def _calls_to_first_valid(rounds: list[RoundResult], field_name: str) -> int | None:
    total = 0
    for round_result in rounds:
        total += int(getattr(round_result, field_name, 0) or 0)
        if round_result.valid:
            return total
    return None


def _band_for_rank(rank: Any) -> str | None:
    try:
        value = int(rank)
    except (TypeError, ValueError):
        return None
    if value < 20:
        return "band1"
    if value < 40:
        return "band2"
    if value < 60:
        return "band3"
    return "band4"


def _time_to_first_valid(rounds: list[RoundResult]) -> float | None:
    total = 0.0
    for round_result in rounds:
        total += round_result.wall_time_seconds
        if round_result.valid:
            return total
    return None


def _safe_path_name(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", value).strip("._") or "problem"


def _hash_text(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


