def _merge_semantic_payloads(primary: dict[str, Any], secondary: dict[str, Any]) -> dict[str, Any]:
    merged = dict(primary)
    primary_diag = _advisory_diagnosis_from_payload(primary)
    secondary_diag = _advisory_diagnosis_from_payload(secondary)
    primary_errors = list(primary_diag.get("diagnosed_errors") or [])
    secondary_errors = list(secondary_diag.get("diagnosed_errors") or [])
    seen_errors = {
        (str(item.get("type")), str(item.get("description")))
        for item in primary_errors
        if isinstance(item, dict)
    }
    for item in secondary_errors:
        if not isinstance(item, dict):
            continue
        key = (str(item.get("type")), str(item.get("description")))
        if key not in seen_errors:
            primary_errors.append(item)
            seen_errors.add(key)
    instructions = _dedupe_strings(
        [
            *_string_list(primary_diag.get("repair_instructions")),
            *_string_list(secondary_diag.get("repair_instructions")),
        ]
    )
    if primary_diag:
        primary_diag["diagnosed_errors"] = primary_errors
        primary_diag["repair_instructions"] = instructions
        merged["advisory_diagnosis"] = primary_diag
    merged["diagnosed_error_types"] = _dedupe_strings(
        [
            *_string_list(primary.get("diagnosed_error_types")),
            *_string_list(secondary.get("diagnosed_error_types")),
        ]
    )
    merged["repair_instructions"] = instructions or _dedupe_strings(
        [
            *_string_list(primary.get("repair_instructions")),
            *_string_list(secondary.get("repair_instructions")),
        ]
    )
    merged["feedback"] = "; ".join(_dedupe_strings([str(primary.get("feedback") or ""), str(secondary.get("feedback") or "")]))
    merged["spec_comparison_result"] = secondary
    if isinstance(merged.get("advisory_diagnosis"), dict):
        merged["advisory_diagnosis"]["spec_comparison_result"] = secondary
    return merged


def _spec_comparison_text(payload: dict[str, Any]) -> str | None:
    if not payload:
        return None
    diagnosis = _advisory_diagnosis_from_payload(payload)
    instructions = _string_list(diagnosis.get("repair_instructions"))
    errors = diagnosis.get("diagnosed_errors") if isinstance(diagnosis, dict) else []
    return json.dumps(
        {
            "diagnosed_errors": errors,
            "repair_instructions": instructions,
            "should_execute": payload.get("should_execute"),
            "score": payload.get("score"),
        },
        ensure_ascii=False,
    )


def _dedupe_strings(values: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for value in values:
        text = str(value).strip()
        if text and text not in seen:
            seen.add(text)
            result.append(text)
    return result


