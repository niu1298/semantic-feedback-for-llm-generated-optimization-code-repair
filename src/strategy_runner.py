STRATEGY_STAGES: dict[str, tuple[str, ...]] = {
    "vanilla": ("generate",),
    "execution_only": ("generate", "execute", "execution_feedback"),
    "static_execution": ("generate", "static_check", "execute"),
    "semantic_execution": ("generate", "semantic_check", "execute", "execution_feedback"),
    "semantic_advisory_execution": (
        "generate",
        "semantic_check",
        "execute",
        "execution_feedback",
    ),
    "static_semantic_execution": (
        "generate",
        "static_check",
        "semantic_check",
        "execute",
        "execution_feedback",
    ),
}
SUPPORTED_STRATEGIES = tuple(STRATEGY_STAGES)


@dataclass(slots=True)
class StrategyRunner:
    config: ExperimentConfig
    llm_client: Any | None = None
    semantic_client: Any | None = None
    executor_fn: Callable[..., dict[str, Any]] = execute_generated_code
    semantic_checker_fn: Callable[..., Any] = check_semantics

    def validate_strategies(self) -> None:
        unknown = sorted(set(self.config.strategies) - set(SUPPORTED_STRATEGIES))
        if unknown:
            raise ValueError(f"Unsupported strategies: {', '.join(unknown)}")

    def stages_for(self, strategy: str) -> tuple[str, ...]:
        try:
            return STRATEGY_STAGES[strategy]
        except KeyError as exc:
            raise ValueError(f"Unsupported strategy: {strategy}") from exc

    def run_placeholder(self) -> ExperimentResult:
        self.validate_strategies()
        if self.config.problem_source == "synthetic":
            problem_ids = self.config.problem_ids or [
                f"synthetic_{index:03d}" for index in range(1, self.config.problem_limit + 1)
            ]
            problem_ids = problem_ids[: self.config.problem_limit]
        else:
            adapter = ORThoughtAdapter(
                dataset_name=self.config.dataset_name,
                dataset_root=self.config.dataset_root,
            )
            problem_ids = adapter.sample_problem_ids(limit=self.config.problem_limit)
        problems: list[ProblemResult] = []

        for strategy in self.config.strategies:
            stages = ", ".join(self.stages_for(strategy))
            for problem_id in problem_ids:
                round_result = RoundResult(
                    strategy=strategy,
                    problem_id=problem_id,
                    round_index=0,
                    valid=None,
                    error_type="not_run",
                    notes=[
                        "Placeholder only: no LLM, semantic checker, solver, or ORThought call was made.",
                        f"Intended stages: {stages}.",
                    ],
                )
                problems.append(
                    ProblemResult(
                        strategy=strategy,
                        problem_id=problem_id,
                        rounds=[round_result],
                        final_valid=None,
                        notes=["Skeleton placeholder result."],
                    )
                )

        return ExperimentResult(
            created_at=datetime.now(timezone.utc).isoformat(),
            config=self.config.to_dict(),
            problems=problems,
            notes=["Generated by run_pilot.py placeholder mode."],
        )

    def run_vanilla(self, run_dir: Path) -> ExperimentResult:
        if self.config.strategy not in {
            "vanilla",
            "static_execution",
            "execution_only",
            "semantic_execution",
            "semantic_advisory_execution",
        }:
            raise NotImplementedError(
                "Implemented strategies are: vanilla, static_execution, execution_only, "
                "semantic_execution, semantic_advisory_execution."
            )
        if self.config.problem_source != "orthought":
            raise NotImplementedError(
                "API-backed runs require problem_source='orthought'. Use --dry-run with "
                "configs/smoke/offline_synthetic.yaml for offline checks."
            )

        adapter = ORThoughtAdapter(
            dataset_name=self.config.dataset_name,
            dataset_root=self.config.dataset_root,
        )
        problems_data = self._load_selected_problems(adapter, run_dir)
        client = self.llm_client or self._make_llm_client()
        problem_results = [
            self._run_problem(problem, client, run_dir)
            for problem in problems_data
        ]

        return ExperimentResult(
            created_at=datetime.now(timezone.utc).isoformat(),
            config=self.config.to_dict(),
            problems=problem_results,
            notes=[]
            if self.config.strategy in {"semantic_execution", "semantic_advisory_execution"}
            else ["No semantic checker was run."],
        )

    def run_real(self, run_dir: Path) -> ExperimentResult:
        return self.run_vanilla(run_dir)

