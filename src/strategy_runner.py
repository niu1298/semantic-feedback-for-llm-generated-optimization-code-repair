def _optional_float(value: Any) -> float | None:
    if value is None:
        return None
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def _optional_bool(value: Any) -> bool | None:
    if value is None:
        return None
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        lowered = value.strip().lower()
        if lowered in {"true", "1", "yes"}:
            return True
        if lowered in {"false", "0", "no"}:
            return False
        if lowered in {"", "none", "null"}:
            return None
    return bool(value)


def _optional_str(value: Any) -> str | None:
    if value is None:
        return None
    text = str(value)
    return text if text else None


def _string_list(value: Any) -> list[str]:
    if not isinstance(value, list):
        return []
    return [str(item) for item in value]


def _feedback_for_repair(feedback: dict[str, Any], use_expected_objective: bool) -> dict[str, Any]:
    if use_expected_objective:
        return dict(feedback)
    repaired = dict(feedback)
    repaired["expected_objective"] = None
    repaired["objective_gap"] = None
    return repaired


def _expected_objective(problem: dict[str, object]) -> float | None:
    metadata = problem.get("metadata")
    if not isinstance(metadata, dict):
        return None
    candidates: list[Any] = []
    answer = metadata.get("answer")
    if isinstance(answer, dict):
        candidates.append(answer.get("obj"))
        candidates.append(answer.get("objective"))
    summary = metadata.get("summary")
    if isinstance(summary, dict):
        candidates.append(summary.get("ground_truth"))
        candidates.append(summary.get("obj"))
    for value in candidates:
        if value is None:
            continue
        try:
            return float(value)
        except (TypeError, ValueError):
            continue
    return None


