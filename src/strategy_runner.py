def _static_check_summary(static_result: Any) -> dict[str, Any]:
    checks = list(getattr(static_result, "checks", []) or [])
    failed_checks: list[str] = []
    num_errors = 0
    num_warnings = 0
    for item in checks:
        if not isinstance(item, dict) or item.get("passed") is not False:
            continue
        failed_checks.append(str(item.get("check_name") or "unknown"))
        severity = str(item.get("severity") or "")
        if severity == "error":
            num_errors += 1
        elif severity == "warning":
            num_warnings += 1
    return {
        "num_errors": num_errors,
        "num_warnings": num_warnings,
        "failed_checks": failed_checks,
    }


def _advisory_diagnosis_from_payload(payload: dict[str, Any]) -> dict[str, Any]:
    diagnosis = payload.get("advisory_diagnosis")
    return dict(diagnosis) if isinstance(diagnosis, dict) else {}


def _diagnosed_error_types(advisory_diagnosis: dict[str, Any]) -> list[str]:
    errors = advisory_diagnosis.get("diagnosed_errors")
    if not isinstance(errors, list):
        return []
    types: list[str] = []
    for item in errors:
        if isinstance(item, dict) and item.get("type") is not None:
            types.append(str(item["type"]))
    return types


def _execution_status(execution: dict[str, Any], error_type: str | None) -> str:
    if execution.get("executed"):
        if execution.get("timeout"):
            return "timeout"
        if error_type in {"compile_failed", "runtime_error", "no_objective"}:
            return str(error_type)
        if execution.get("returncode") == 0:
            return "success"
        return "runtime_error"
    if error_type == "compile_failed":
        return "compile_failed"
    if error_type in {"semantic_reject", "rule_semantic_reject", "skipped_by_gate"}:
        return "skipped_by_gate"
    if error_type == "llm_failed":
        return "unknown"
    return "unknown"


